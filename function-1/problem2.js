function difference(x,y){
  return x-y;
}
let input1 =difference(8,4);
let input2=difference(4,18);

function print_absolute(answer){
  if(answer<0){
    return (0-answer);
  }
  else{
    return answer
  }
}
console.log(print_absolute(input1));
console.log(print_absolute(input2));









































































  




  
















